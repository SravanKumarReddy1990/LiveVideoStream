package com.bsmaps.chatting;

public class Ffriends {

    int image;
    String name, team;

    public Ffriends(int image, String name, String team) {
        this.image = image;
        this.name = name;
        this.team = team;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public String getTeam() {
        return team;
    }
}
